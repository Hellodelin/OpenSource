package xin.xihc.utils.flatmap;

/**
 * 扁平化Map值类型
 *
 * @Author Leo.Xi
 * @Date 2019/1/18 14:45
 * @Version 1.0
 **/
public enum FlatMapType {

    /**
     * 对象
     */
    OBJECT,

    /**
     * Map
     */
    MAP,

    /**
     * 整型
     */
    INTEGER,

    /**
     * 长整型
     */
    LONG,

    /**
     * 字符串
     */
    STRING,

    /**
     * 列表
     */
    LIST,

    /**
     * 浮点
     */
    DOUBLE,

    /**
     * 布尔
     */
    BOOLEAN;

}
